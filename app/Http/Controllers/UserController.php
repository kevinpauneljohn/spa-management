    public function check_user_logged_in_password(UserService $userService, Request $request)
    {
        if($userService->check_user_password($request->input('password')) )
        {
            //this will ensure that the password must be validated first before updating the inventories
            $request->session()->put('password_matched',true);

            return response()->json(['success' => true, 'message' => 'Password Matched']);
        }

        return response()->json(['success' => false, 'message' => 'You are not allowed to update this record']);
    }
